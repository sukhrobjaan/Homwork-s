package lesson12.conculiyator;

public class Arfimetik {


    public double add(double a,double b){
        return a+b;
    }
    public double sub(double a,double b){
        return a-b;
    }
    public double mul(double a,double b){
        return a*b;
    }
    public double div(double a,double b){
        return a/b;
    }

    public double pow2(double a){
        return Math.pow(a,2);
    }
    public double pow3(double a){
        return Math.pow(a,3);
    }
    public double sqrt2(double a){
        return Math.pow(a,1.0/2);
    }
    public double sqrt3(double a){
        return Math.pow(a,1.0/3);
    }
    public double cos(double a){
        return Math.cos(a);
    }
    public double sin(double a){
        return Math.sin(a);
    }
    public double percentage(double a){
        return a/100.0;
    }
    public double reverse(double a){
        return 1.0/a;
    }
    public double c(){
        return 0.0;
    }
    public double clear(){
        return 0;
    }

}
