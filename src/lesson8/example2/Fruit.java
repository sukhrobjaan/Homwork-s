package lesson8.example2;

public class Fruit {

    String fruitName;
    String fruitColor;
    int weight;


    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public Fruit(String fruitName, String fruitColor, int weight) {
        this.fruitName = fruitName;
        this.fruitColor = fruitColor;
        this.weight = weight;
    }






}


